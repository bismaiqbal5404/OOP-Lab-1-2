
package java_4;


public class Java_4 {

   
    public static void main(String[] args) {
         double F;
        double C;
        C = 289;
        F = (C * 9 / 5) + 32;
        System.out.println("For C = 289, F = " + F);

     
        C = 400;
        F = (C * 9 / 5) + 32;
        System.out.println("For C = 400, F = " + F);

    
        C = -36;
        F = (C * 9 / 5) + 32;
        System.out.println("For C = -36, F = " + F);

       
        C = -180;
        F = (C * 9 / 5) + 32;
        System.out.println("For C = -180, F = " + F);
    }
}
